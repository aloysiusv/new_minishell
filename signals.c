#include "minishell.h"

void	handle_signals(int sig)
{
	if (sig == SIGQUIT)
	{
		ft_putstr_fd("\b\b  \b\b  \b\b", 1);
		g_exit_code = 127;
	}
	if (sig == SIGINT)
	{
		write(1, "\n", 1);
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
		g_exit_code = 130;
	}
}
